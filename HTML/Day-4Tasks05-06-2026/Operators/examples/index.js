// Arithmetic Operator (Discount)
let productPrice = 1500;
let discount = 200;

let finalPrice = productPrice - discount;

console.log("Final Price:", finalPrice);

// Assignment Operator (Bank balance)
let balance = 5000;

balance += 2000; // credited Money
balance -= 1000; // payment

console.log("Current Balance:", balance);

//comparison (Eligible)
let age = 20;

console.log("Eligible to Vote:", age >= 18);

//Logical Operator (Login)
let username = "admin";
let password = "12345";

let loginSuccess =
    username === "admin" &&
    password === "0000";

console.log("Login Success:", loginSuccess);

// Ternary (pass/fail)
let marks = 45;

let result = marks >= 35 ? "Pass" : "Fail";

console.log(result);


