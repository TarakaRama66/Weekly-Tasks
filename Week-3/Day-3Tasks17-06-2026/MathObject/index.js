let number = 4.7;

console.log("Original Number:", number);

// Round to nearest integer
console.log("Math.round():", Math.round(number));

// Round down
console.log("Math.floor():", Math.floor(number));

// Round up
console.log("Math.ceil():", Math.ceil(number));

// Remove decimal part
console.log("Math.trunc():", Math.trunc(number));

// Absolute value
console.log("Math.abs(-10):", Math.abs(-10));

// Power
console.log("Math.pow(2, 3):", Math.pow(2, 3));

// Square Root
console.log("Math.sqrt(64):", Math.sqrt(64));

// Maximum Value
console.log("Math.max(10, 20, 30):", Math.max(10, 20, 30));

// Minimum Value
console.log("Math.min(10, 20, 30):", Math.min(10, 20, 30));

// Random Number (1 to 10)
console.log("Math.random():", Math.random());

// Random Number (1 to 10)
console.log("Random 1-10:", Math.floor(Math.random() * 10) + 1);